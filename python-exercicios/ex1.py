num1 = int(input("Digite um número: "))
num2 = int(input("Digite o segundo número: "))

soma = num1 + num2
print(f"A soma dos dois números digitados é {soma}!")
subtracao = num1 - num2
print(f"A subtração dos dois números digitados é {subtracao}!")
multi = num1 * num2
print(f"A multiplicação dos dois números digitados é {multi}!")
divisao = num1 / num2
print(f"A divisão dos dois números digitados é {divisao}!")